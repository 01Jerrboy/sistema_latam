drop database if exists bd_latam;
create database bd_latam;

use bd_latam;

CREATE TABLE `pais` (
  `id_pais` int(11) NOT NULL primary key,
  `nombre_pais` varchar(70) DEFAULT NULL
) ;

CREATE TABLE aeropuerto (
    id_aeropuerto INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    codigo VARCHAR(10) UNIQUE NOT NULL
);


CREATE TABLE avion (
    id_avion INT AUTO_INCREMENT PRIMARY KEY,
    modelo VARCHAR(50) NOT NULL,
    capacidad INT NOT NULL,
    fabricante VARCHAR(50),
    año_fabricacion YEAR
);

CREATE TABLE destino (
    id_destino INT PRIMARY KEY AUTO_INCREMENT,
    ciudad VARCHAR(100) NOT NULL,
    id_aeropuerto INT,
    id_pais INT,
    foreign key(id_pais) references pais(id_pais),
    foreign key(id_aeropuerto) references aeropuerto(id_aeropuerto)
);

CREATE TABLE clase_vuelo(
	id_clase_vuelo INT PRIMARY KEY AUTO_INCREMENT,
    descripcion VARCHAR(100) UNIQUE NOT NULL
);

CREATE TABLE vuelo (
    id_vuelo INT PRIMARY KEY AUTO_INCREMENT,
    id_aeropuerto INT NOT NULL,
    id_clase_vuelo INT NOT NULL,
    id_avion INT NOT NULL,
    id_destino_origen INT NOT NULL,
    id_destino_regreso INT NULL,
    fecha_salida DATE NULL,
    hora_salida TIME NULL,
    fecha_regreso DATE,
    hora_regreso TIME,
    tiempo_vuelo TIME,
    tipo_vuelo ENUM('Ida', 'Ida y Vuelta') DEFAULT 'Ida',
    costo_vuelo DECIMAL(10,2),
    FOREIGN KEY (id_avion) REFERENCES avion(id_avion),
	FOREIGN KEY (id_clase_vuelo) REFERENCES clase_vuelo(id_clase_vuelo),
    FOREIGN KEY (id_destino_origen) REFERENCES destino(id_destino),
    FOREIGN KEY (id_destino_regreso) REFERENCES destino(id_destino)
);


